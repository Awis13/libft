#include <stdio.h>

int	ft_isalpha(int argument)
{
	char	c;

	c = argument + 0;
	if (c >= 'a' && c <= 'z' || c >= 'A' && c <= 'Z')
	{
		return (1);
	}
	else
		return (0);
}
